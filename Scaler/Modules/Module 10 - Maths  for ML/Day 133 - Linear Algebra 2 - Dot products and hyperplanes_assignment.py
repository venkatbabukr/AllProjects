import math

def dotProduct(v1, v2):
    return sum ((a*b) for a, b in zip(v1, v2))

def magnitude(v):
    return math.sqrt(dotProduct(v, v))

def angleBtnTwoVectors(v1, v2):
    v1_dot_v2 = dotProduct(v1, v2)
    if v1_dot_v2 == 0:
        return math.pi / 2
    else :
        return math.acos(dotProduct(v1, v2) / (magnitude(v1) * magnitude (v2)))

