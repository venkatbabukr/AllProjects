"""**AQ5. Least frequent from back**

You are given a string of characters, these characters can be anything from alphabets to symbols. Your task is to complete the given function so that it returns the last character that repeated the least times in string. If no such character is present then it should return None
"""

def count_char(str):
  # write your code here
  char_count = {}

  for c in str:
    if c in char_count:
      char_count[c] += 1
    else:
      char_count[c] = 1
  least_count = min(char_count.values(), default = 0)
  return next((c for c in reversed(str) if char_count[c] == least_count), None)

str = "aaaadfhgajshfdmmmssssmmm"
print(f"count_char('{str}') = {count_char(str)}")